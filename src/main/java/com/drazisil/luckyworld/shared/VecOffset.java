package com.drazisil.luckyworld.shared;

public class VecOffset {
    private final double x;
    private final double y;
    private final double z;

    public VecOffset(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
}
